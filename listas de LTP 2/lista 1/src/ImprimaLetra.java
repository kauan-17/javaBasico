
public class ImprimaLetra {

	public static void main(String[] args) {
		char letra = 'Z'; // Inicializa a variável "letra" com o valor 'Z'

		// Usa um loop "for" para imprimir as letras de 'Z' até 'A' em ordem decrescente
		for (letra = 'Z'; letra >= 'A'; letra--) {
			System.out.print(letra + " "); // Imprime a letra atual, seguida de um espaço
		}

	}
}
